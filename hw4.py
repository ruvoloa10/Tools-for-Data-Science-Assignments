#g. Select one image with number “5”, add Gaussian noise to it, and display the noisy image. Denoise the image using rank-k approximations with k = 5, 10, and 20.

rng = np.random.default_rng(12345)
idx = 5
x_original = X_raw[idx]
x_noisy = x_original + rng.normal(loc=0.0, scale=4.0, size=x_original.shape)

ks = [5, 10, 20]
denoised_images = []
mses = []

for k in ks:
    # Rank-k approximation basis
    Vk = Vt[:k, :]          # shape (k, 64)

    # Project noisy image onto first k PCs
    coeffs = x_noisy @ Vk.T  # shape (k,)

    # Reconstruct
    x_denoised = coeffs @ Vk  # shape (64,)

    # Clip to valid pixel range
    x_denoised = np.clip(x_denoised, 0, 16)

    denoised_images.append(x_denoised)


    mse = mean_squared_error(x_original, x_denoised)
    mses.append(mse)

# Plot original, noisy, and denoised images
n_cols = len(ks) + 2
plt.figure(figsize=(2.5 * n_cols, 3))

