plt.imshow(x_noisy.reshape(8, 8), cmap='gray')
plt.title("Noisy")
plt.axis('off')

for i, (k, img_k) in enumerate(zip(ks, denoised_images), start=3):
    plt.subplot(1, n_cols, i)
    plt.imshow(img_k.reshape(8, 8), cmap='gray')
    plt.title(f"k = {k}")
    plt.axis('off')

